import { writeFile } from 'node:fs/promises';
import Ajv2020 from 'ajv/dist/2020.js';
import addFormats from 'ajv-formats';
import standaloneCode from 'ajv/dist/standalone/index.js';
import browserSchema from '../../protocol/browser-v1.schema.json' with { type: 'json' };

const ajv = new Ajv2020({
  allErrors: true,
  strict: true,
  strictTypes: false,
  strictRequired: false,
  code: { source: true, esm: true },
});
addFormats(ajv);
const validate = ajv.compile(browserSchema);
const generated = standaloneCode(ajv, validate)
  .replaceAll('require("ajv-formats/dist/formats").fullFormats', 'fullFormats')
  .replaceAll('require("ajv/dist/runtime/ucs2length").default', 'ucs2length')
  .replaceAll('require("ajv/dist/runtime/equal").default', 'equal');
const banner = `// @ts-nocheck
// Generated from protocol/browser-v1.schema.json by scripts/generate-protocol-validator.mjs.
// Do not edit this file directly.
import { fullFormats } from 'ajv-formats/dist/formats';
import ucs2length from 'ajv/dist/runtime/ucs2length';
import equal from 'ajv/dist/runtime/equal';
`;

await writeFile(new URL('../src/protocol/generated-validator.ts', import.meta.url), `${banner}${generated}\n`, 'utf8');
